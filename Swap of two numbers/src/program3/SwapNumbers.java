package program3;

public class SwapNumbers {
	public static void main(String[] args) {
		int a=44,b=99,temp;
		System.out.println("before swap");
		System.out.println("value of a: "+a);
		System.out.println("value of b: "+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swap");
		System.out.println("value of a:"+a);
		System.out.println("value of b:"+b);
		
	}

}
